#include "libft.h"

int		ft_nbwords(char *str, char c)
{
	int i;
	int cpt;

	i = 0;
	cpt = 0;
	while (str[i])
	{
		if (str[i] != c && (str[i - 1] == c || i == 0))
		{
			cpt++;
			i++;
		}
		else
			i++;
	}
	return (cpt);
}
